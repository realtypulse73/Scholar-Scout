#!/usr/bin/env node

import { access, mkdir, writeFile } from 'node:fs/promises';
import { randomBytes } from 'node:crypto';
import path from 'node:path';

const args = parseArgs(process.argv.slice(2));
const localFile = path.resolve(args.localFile || '.env.prelaunch.local');
const reportFile = path.resolve(
  args.reportFile || 'reports/environment-provisioning.md',
);
const staffEmail = args.staffEmail || 'staff@example.org';
const localWebUrl = args.localWebUrl || 'http://localhost:3000';
const localDataUrl = args.localDataUrl || 'http://localhost:4010/scholarscout';

if (!args.force && (await exists(localFile))) {
  console.error(
    `Local env file already exists at ${localFile}. Re-run with --force to replace it.`,
  );
  process.exit(1);
}

await mkdir(path.dirname(localFile), { recursive: true });
await mkdir(path.dirname(reportFile), { recursive: true });

const nextAuthSecret = token(48);
const healthToken = token(32);
const dataServiceToken = token(32);

await writeFile(
  localFile,
  [
    '# ScholarScout local prelaunch environment',
    '# Generated by npm run provision:env. Do not commit this file.',
    '',
    `NEXTAUTH_URL=${localWebUrl}`,
    `NEXTAUTH_SECRET=${nextAuthSecret}`,
    '',
    'SCHOLARSCOUT_ALLOW_CREDENTIALS_ONLY_PRODUCTION=true',
    `SCHOLARSCOUT_STAFF_EMAILS=${staffEmail}`,
    `SCHOLARSCOUT_HEALTH_TOKEN=${healthToken}`,
    '',
    'SCHOLARSCOUT_DATA_ADAPTER=http',
    `SCHOLARSCOUT_DATA_SERVICE_URL=${localDataUrl}`,
    `SCHOLARSCOUT_DATA_SERVICE_TOKEN=${dataServiceToken}`,
    '',
    `SCHOLARSCOUT_SMOKE_BASE_URL=${localWebUrl}`,
    `SCHOLARSCOUT_SMOKE_HEALTH_TOKEN=${healthToken}`,
    'SCHOLARSCOUT_SMOKE_EXPECTED_ADAPTER=http',
    'SCHOLARSCOUT_SMOKE_EXPECTED_PROVIDERS=',
    'SCHOLARSCOUT_SMOKE_MAX_HEALTH_AGE_MINUTES=5',
    'SCHOLARSCOUT_SMOKE_TIMEOUT_MS=10000',
    'SCHOLARSCOUT_SMOKE_RETRIES=1',
    'SCHOLARSCOUT_SMOKE_MAX_LATENCY_MS=',
    '',
  ].join('\n'),
);

await writeFile(
  reportFile,
  [
    '# ScholarScout Environment Provisioning',
    '',
    `Generated: ${new Date().toISOString()}`,
    '',
    '## Local Rehearsal',
    '',
    `- Local env file: \`${path.relative(process.cwd(), localFile)}\``,
    '- Status: generated with local-only credentials and localhost URLs.',
    '- Boundary: this is for prelaunch plumbing only, not public production traffic.',
    '',
    'Run:',
    '',
    '```bash',
    `npm run rehearse:prelaunch -- --skip-smoke --env-file ${path.relative(process.cwd(), localFile)}`,
    '```',
    '',
    '## Production Values Still Needed',
    '',
    '- `NEXTAUTH_URL`: deployed HTTPS ScholarScout URL.',
    '- `NEXTAUTH_SECRET`: long random Auth.js secret in the hosting environment.',
    '- Google or GitHub OAuth client id and secret.',
    '- OAuth callback URL configured with the provider.',
    '- `SCHOLARSCOUT_STAFF_EMAILS`: trusted staff allowlist.',
    '- `SCHOLARSCOUT_HEALTH_TOKEN`: monitoring bearer token.',
    '- Durable data adapter credentials: Vercel Blob token or hosted HTTP service URL/token.',
    '- GitHub Actions secrets for readiness, smoke, and prelaunch workflows.',
    '',
    '## Recommended Next External Actions',
    '',
    '1. Choose production data adapter: `vercel-blob` for Vercel-native pilot storage, or `http` for a hosted service/CMS boundary.',
    '2. Create OAuth app credentials for Google or GitHub.',
    '3. Link/deploy the app in the hosting provider.',
    '4. Add the production values to hosting and GitHub Actions secrets.',
    '5. Run the prelaunch rehearsal workflow against the deployed URL.',
    '',
  ].join('\n'),
);

console.log(`Generated local env file: ${localFile}`);
console.log(`Generated provisioning report: ${reportFile}`);

function parseArgs(values) {
  const parsed = {
    force: false,
    localFile: '',
    reportFile: '',
    staffEmail: '',
    localWebUrl: '',
    localDataUrl: '',
  };

  for (let index = 0; index < values.length; index += 1) {
    const value = values[index];

    if (value === '--force') {
      parsed.force = true;
    } else if (value === '--local-file') {
      parsed.localFile = values[index + 1] ?? '';
      index += 1;
    } else if (value === '--report-file') {
      parsed.reportFile = values[index + 1] ?? '';
      index += 1;
    } else if (value === '--staff-email') {
      parsed.staffEmail = values[index + 1] ?? '';
      index += 1;
    } else if (value === '--local-web-url') {
      parsed.localWebUrl = values[index + 1] ?? '';
      index += 1;
    } else if (value === '--local-data-url') {
      parsed.localDataUrl = values[index + 1] ?? '';
      index += 1;
    }
  }

  return parsed;
}

function token(byteLength) {
  return randomBytes(byteLength).toString('base64url');
}

async function exists(filePath) {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
}
